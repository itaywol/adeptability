// loadFixtureAdapter loads testdata/<dir>/adapter.yaml through the validator
// + loader pipeline so tests exercise the same path production code uses.
func loadFixtureAdapter(t *testing.T, dir string) adept.HarnessAdapter {
	t.Helper()
	v, err := NewSchemaValidator()
	require.NoError(t, err)
	loader := NewLoader(v, nil, nil)
	a, err := loader.LoadFile(filepath.Join("testdata", dir, "adapter.yaml"))
	require.NoError(t, err)
	return a
}

// writeFile is a tiny helper that creates parent dirs and writes content.
func writeFile(t *testing.T, path, content string) {
	t.Helper()
	require.NoError(t, os.MkdirAll(filepath.Dir(path), 0o755))
	require.NoError(t, os.WriteFile(path, []byte(content), 0o644))
}

// TestImport_PerSkill_ReversesRenameAndBodyWrappers verifies the full per-skill
// reverse pipeline against the Junie-style fixture: rename inverted, prefix +
// suffix stripped, default activation applied, replace-rule warning emitted.
func TestImport_PerSkill_ReversesRenameAndBodyWrappers(t *testing.T) {
	a := loadFixtureAdapter(t, "import-per-skill")
	root := t.TempDir()
	writeFile(t, filepath.Join(root, ".junie", "guidelines", "alpha.md"),
		"---\ndesc: alpha description\ntags:\n  - first\n  - second\n---\n"+
			"> Auto-generated by adeptability. Do not edit by hand.\n\n"+
			"body content here\n"+
			"\n<!-- end -->")
	writeFile(t, filepath.Join(root, ".junie", "guidelines", "beta.md"),
		"---\ndesc: beta description\n---\n"+
			"> Auto-generated by adeptability. Do not edit by hand.\n\n"+
			"beta body\n"+
			"\n<!-- end -->")

	out, err := a.Import(context.Background(), root)
	require.NoError(t, err)
	require.Len(t, out, 2)

	require.Equal(t, "alpha", out[0].Skill.ID)
	require.Equal(t, "alpha description", out[0].Skill.Description)
	require.Equal(t, []string{"first", "second"}, out[0].Skill.Tags)
	require.Equal(t, adept.ActivationAgent, out[0].Skill.Activation)
	require.Equal(t, "body content here\n", out[0].Skill.Body)
	require.Contains(t, out[0].Warnings, importedReplaceWarning)
	require.Equal(t, filepath.Join(root, ".junie", "guidelines", "alpha.md"), out[0].SourcePath)

	require.Equal(t, "beta", out[1].Skill.ID)
	require.Equal(t, "beta description", out[1].Skill.Description)
}

