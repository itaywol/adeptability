// TestImport_PerSkill_Sidecars verifies needs-directory adapters collect every
// non-SKILL.md file beside SKILL.md into Skill.Files.
func TestImport_PerSkill_Sidecars(t *testing.T) {
	a := loadFixtureAdapter(t, "import-per-skill-sidecars")
	root := t.TempDir()
	skillDir := filepath.Join(root, ".clone", "skills", "echo")
	writeFile(t, filepath.Join(skillDir, "SKILL.md"),
		"---\nsummary: echo a thing\nglobs:\n  - 'src/**/*.go'\n---\nmain body\n")
	writeFile(t, filepath.Join(skillDir, "scripts", "run.sh"), "#!/bin/sh\necho hi\n")
	writeFile(t, filepath.Join(skillDir, "references", "notes.md"), "see also\n")

	out, err := a.Import(context.Background(), root)
	require.NoError(t, err)
	require.Len(t, out, 1)
	require.Equal(t, "echo", out[0].Skill.ID)
	require.Equal(t, "echo a thing", out[0].Skill.Description)
	require.Equal(t, []string{"src/**/*.go"}, out[0].Skill.Globs)
	// Sidecars sorted by RelPath.
	require.Len(t, out[0].Files, 2)
	paths := []string{out[0].Files[0].RelPath, out[0].Files[1].RelPath}
	require.Equal(t, []string{"references/notes.md", "scripts/run.sh"}, paths)
	// No replace rules in this adapter — no warnings.
	require.Empty(t, out[0].Warnings)
}

