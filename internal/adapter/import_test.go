// TestImport_RoundTrip_PerSkill confirms render → import is approximately
// idempotent for the per-skill case: the recovered Skill matches the inputs.
func TestImport_RoundTrip_PerSkill(t *testing.T) {
	a := loadFixtureAdapter(t, "import-per-skill-sidecars")
	root := t.TempDir()
	skill := &adept.Skill{
		ID:          "round",
		Description: "trip back",
		Activation:  adept.ActivationGlobs,
		Globs:       []string{"docs/**/*.md"},
		Body:        "core body content\n",
	}
	rendered, err := a.Renderer().Render(context.Background(), adept.RenderInput{
		Skill:   skill,
		Harness: a.Spec(),
		Project: adept.ProjectInfo{Name: "demo"},
	})
	require.NoError(t, err)
	// Write the rendered file to the fake project root.
	abs := filepath.Join(root, rendered.Path)
	require.NoError(t, os.MkdirAll(filepath.Dir(abs), 0o755))
	require.NoError(t, os.WriteFile(abs, rendered.Bytes, 0o644))

	out, err := a.Import(context.Background(), root)
	require.NoError(t, err)
	require.Len(t, out, 1)
	require.Equal(t, "round", out[0].Skill.ID)
	require.Equal(t, "trip back", out[0].Skill.Description)
	require.Equal(t, adept.ActivationGlobs, out[0].Skill.Activation)
	require.Equal(t, []string{"docs/**/*.md"}, out[0].Skill.Globs)
	require.Equal(t, "core body content\n", out[0].Skill.Body)
}

// TestImport_ImportRenameOverride exercises the explicit spec.Import.Rename
// hint. We build the synthetic adapter directly because the loader yaml has
// no need for the hint in normal use.
func TestImport_ImportRenameOverride(t *testing.T) {
	spec := Spec{
		ID:     "junie-override",
		Kind:   adept.KindPerSkill,
		Output: ".x/{id}.md",
		Frontmatter: Frontmatter{
			// Forward rename is non-bijective (two canonical keys both map
			// to "summary"); explicit reverse hint disambiguates.
			Rename: map[string]string{"description": "summary"},
		},
		Import: Import{
			Rename: map[string]string{"headline": "description"},
		},
	}
	a, err := NewSynthetic(spec)
	require.NoError(t, err)
	root := t.TempDir()
	writeFile(t, filepath.Join(root, ".x", "z.md"),
		"---\nheadline: explicit\n---\nbody\n")
	out, err := a.Import(context.Background(), root)
	require.NoError(t, err)
	require.Len(t, out, 1)
	require.Equal(t, "explicit", out[0].Skill.Description)
}

