// TestImport_AggregatorPerGlob_FrontmatterActivation exercises both the
// {glob}-templated walk and the applyTo-style frontmatter that supplies
// activation info to every skill in the bucket.
func TestImport_AggregatorPerGlob_FrontmatterActivation(t *testing.T) {
	a := loadFixtureAdapter(t, "import-aggregator-per-glob")
	root := t.TempDir()
	// Two bucket files; one explicit applyTo + markers, one no markers.
	writeFile(t, filepath.Join(root, ".github", "instructions", "src-ts.instructions.md"),
		"---\napplyTo: 'src/**/*.ts'\n---\n"+
			"<!-- adeptability:begin id=alpha hash=deadbeef -->\n"+
			"## alpha desc\nalpha body\n"+
			"<!-- adeptability:end id=alpha -->\n")
	writeFile(t, filepath.Join(root, ".github", "instructions", "default.instructions.md"),
		"---\napplyTo: '**'\n---\nfreeform default body\n")

	out, err := a.Import(context.Background(), root)
	require.NoError(t, err)
	// Sort defensively (adapter sorts, but we re-sort to be explicit).
	sort.Slice(out, func(i, j int) bool { return out[i].Skill.ID < out[j].Skill.ID })
	require.Len(t, out, 2)

	// alpha came from src-ts.instructions.md → globs activation.
	require.Equal(t, "alpha", out[0].Skill.ID)
	require.Equal(t, adept.ActivationGlobs, out[0].Skill.Activation)
	require.Equal(t, []string{"src/**/*.ts"}, out[0].Skill.Globs)

	// default has no markers → bucket-name fallback id; applyTo:** → always.
	require.Equal(t, "default", out[1].Skill.ID)
	require.Equal(t, adept.ActivationAlways, out[1].Skill.Activation)
}

